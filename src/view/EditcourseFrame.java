    private void loadDataAsync(){
        Cursor oldCursor= getCursor();
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        
        SwingWorker<CourseData, Void> worker= new SwingWorker<>(){
            @Override
            protected CourseData doInBackground() throws Exception{
                Admin admin= adminDAO.getAdminByUserId(currentUser.getId());
                if(admin== null){
                    throw new Exception("admin not found");
                }
                Course course= courseService.getCourseById(courseId, adminId);
                if(course== null){
                    throw new Exception("Course not found");
                }
                return new CourseData(admin.getAdminId(), course);
            }
            @Override 
            protected void done(){
                try{
                    CourseData data= get();
                    EditcourseFrame.this.adminId= data.adminId;
                    EditcourseFrame.this.initial= data.course;
                    //populate fields
                    codeField.setText(data.course.getCourseCode());
                    titleField.setText(data.course.getCourseName());
                    unitField.setText(String.valueOf(data.course.getUnits()));
                    TextArea.setText(data.course.getDescription());
                    saveButton.setEnabled(true);
                }catch(Exception e){
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(EditcourseFrame.this, "Error loading course: "+ e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    new ManageCourseFrame(currentUser).setVisible(true);
                    EditcourseFrame.this.dispose();
                }finally{
                    setCursor(oldCursor);
                }
            }
        };
        worker.execute();
    }
    private static class CourseData{
        final int adminId;
        final Course course;
        
        CourseData(int adminId, Course course){
            this.adminId= adminId;
            this.course= course;
        }
    }
   

