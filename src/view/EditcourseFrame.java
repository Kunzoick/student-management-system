    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try{
        String CourseCode= codeField.getText().trim();
        String CourseName= titleField.getText().trim();
        int Unit= Integer.parseInt(unitField.getText().trim());
        String desc= TextArea.getText().trim();
        //validation
        if(CourseCode.isEmpty() || CourseName.isEmpty() || desc.isEmpty()){
            JOptionPane.showMessageDialog(this, "Pls fill all fileds", "Validation", JOptionPane.WARNING_MESSAGE);
            return;
        }
        // request only for mat203, CS404, STAT
        if(!CourseCode.matches("[A-Z]{2,5}\\d{2,4}")){
            JOptionPane.showMessageDialog(this, "Course code must be 2-5 letters followed by 2-4", "Invalid format", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Course existing= courseService.getCourseByCode(CourseCode, adminId);
        if(existing != null && existing.getCourseId() != this.courseId){
            JOptionPane.showMessageDialog(this, "CourseCode '"+ CourseCode+ "' already exists!", "Duplicate Course", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(Unit <= 0){
            JOptionPane.showMessageDialog(this, "Units must be positive", "Validation", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Course course= new Course();
        course.setCourseId(this.courseId);
        course.setCourseCode(CourseCode);
        course.setCourseName(CourseName);
        course.setUnits(Unit);
        course.setDescription(desc);
        course.setAdminId(this.adminId);
        course.setGradingSystem(initial.getGradingSystem());
        course.setUpdatedAt(LocalDateTime.now());
        
        courseService.updateCourse(course);
        JOptionPane.showMessageDialog(this, "Course updated successfully!");
            new ManageCourseFrame(currentUser).setVisible(true);
            dispose();
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Units must be a valid number!", "Input Error", JOptionPane.WARNING_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error updating course:"+ e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

